/**
 * @file colors.h
 * @brief Коди екранування ANSI для кольорового виводу у терміналі.
 */
#ifndef COLORS_H
#define COLORS_H

// Визначення кольорів ANSI для гарного виводу.

#define NO_COLOR "\x1b[0m"
#define RED "\x1b[31m"
#define GREEN "\x1b[32m"
#define BROWN "\x1b[33m"
#define BLUE "\x1b[34m"
#define MAGENTA "\x1b[35m"
#define CYAN "\x1b[36m"
#define GRAY "\x1b[37m"

#endif // COLORS_H
